    public void doSelectionAction(){
        int[] rows = jTable1.getSelectedRows();
       try{
           FilaItemBuscar row = datamodel.getValueAt(rows[0]);
           //showMsg("Item Selecionado:" + row.getDescItem());
           this.parentFrame.doSelectItem(row, tipo);
           setVisible(false);
        }
        catch(Throwable ex){
            showMsgError(ex);
        }
    }
    
