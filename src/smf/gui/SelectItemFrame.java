    public SelectItemFrame(String tipo, ReportesFrame parentFrame) {
        initComponents();        
        clientesController = new ClientesJpaController(em);
        articulosController = new ArticulosJpaController(em);
        this.tipo = tipo;//A: Articulos, C:Clientess
        this.parentFrame = parentFrame;
        
        initColumns();
        
        if ("A".equalsIgnoreCase(tipo)){
            datamodel = new ItemBuscarDM(columns, articulosController);
        }
        else if ("C".equalsIgnoreCase(tipo)){
            datamodel = new ItemBuscarDM(columns, clientesController);
        }
        
        jTable1.setModel(datamodel);
        jTable1.getTableHeader().addMouseListener(new TableHeadMouseAdapter(jTable1, datamodel));
        
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(!e.getValueIsAdjusting()) {
                    updateStatusSelectBtn();
                }
            }
        });
        
         jTable1.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent mouseEvent) {                
                if (mouseEvent.getClickCount() == 2) {
                    doSelectionAction();                    
                }
            }
        });
        
        doFilter();
    }
    
     public void updateStatusSelectBtn(){
        int[] selectedRows = jTable1.getSelectedRows();
        jButtonSelect.setEnabled( selectedRows.length>0 );
    }
     
    public void initColumns(){        
        
        String[][] columnsMatrix = new String[][] { 
                { "NUI", "cli_ci", "2" }, 
                { "Nombres y Apellidos", "nomapel", "1" },
                { "CODBAR", "art_codbar","2" }, 
                { "Nombre", "art_nombre","1" }
            };
        
        String[] datosColId=null;
        String[] datosColDesc=null;
        if (tipo.equalsIgnoreCase("A")){
            datosColId = columnsMatrix[2];
            datosColDesc = columnsMatrix[3];
        }
        else if (tipo.equalsIgnoreCase("C")){
            datosColId = columnsMatrix[0];
            datosColDesc = columnsMatrix[1];
        }
        
        columns = new ArrayList<>();
        
        columns.add(
                new JTableColumn<FilaItemBuscar>(
                        0,
                        datosColId[0],
                        datosColId[1],
                        String.class,
                        new DefGSVCol<FilaItemBuscar>(){
                            @Override
                            public Object getValueAt(FilaItemBuscar row, int rowIndex) {
                                return row.getCodeItem();
                            }                            
                        }
                )
        );
        
        columns.add(
                new JTableColumn<FilaItemBuscar>(
                        1,
                        datosColDesc[0],
                        datosColDesc[1],
                        String.class,
                        new DefGSVCol<FilaItemBuscar>(){
                            @Override
                            public Object getValueAt(FilaItemBuscar row, int rowIndex) {
                                return row.getDescItem();
                            }                            
                        }
                )
        );
    }
    
